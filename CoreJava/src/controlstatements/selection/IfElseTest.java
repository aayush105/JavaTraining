package controlstatements.selection;

public class IfElseTest {
	/*
	 * ------------------------- If else --------------------
	 * syntax :
	 * if(condition){
	 *	//statement
	 *} else {
	 *	//statement
	 *} 
	 */
	
	public static void main(String[] args) {
		int n = 80;
		if(n%2==0) {
			System.out.println("N is even number");
		} else {
			System.out.println("N is odd number");
		}
	}
}
